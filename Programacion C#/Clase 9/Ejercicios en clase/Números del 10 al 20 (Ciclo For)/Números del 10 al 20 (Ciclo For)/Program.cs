using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Números_del_10_al_20__Ciclo_For_
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 10; i <= 20; i++)
            {
                Console.WriteLine(i);
                Console.ReadLine();
            }
        }
    }
}
